// Helper functions
export function getPopularTools(): Tool[] {
  return tools
    .sort((a, b) => b.searchVolume - a.searchVolume)
    .map((tool) => ({
      ...tool,
      keywords: tool.keywords || [], // Ensure keywords is always an array
    }));
}

export function searchTools(query: string): Tool[] {
  const searchTerm = query.toLowerCase();
  return tools
    .filter(
      (tool) =>
        tool.label !== 'coming-soon' &&
        (tool.name.toLowerCase().includes(searchTerm) ||
          tool.description.toLowerCase().includes(searchTerm) ||
          tool.keywords.some((keyword) =>
            keyword.toLowerCase().includes(searchTerm)
          ))
    )
    .sort((a, b) => b.searchVolume - a.searchVolume);
}

export function getToolsByCategory(categoryId: string): Tool[] {
  return tools
    .filter((tool) => {
      return tool.categories.includes(categoryId);
    })
    .sort((a, b) => {
      // First sort by searchVolume (descending), then by name (ascending) for deterministic order
      if (b.searchVolume !== a.searchVolume) {
        return b.searchVolume - a.searchVolume;
      }
      return a.name.localeCompare(b.name);
    });
}

// O(1) lookup map for tools by ID
export const toolsMap = new Map<string, Tool>(tools.map((t) => [t.id, t]));

export function getToolById(id: string): Tool | undefined {
  return toolsMap.get(id);
}

